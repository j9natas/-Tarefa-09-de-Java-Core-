package LIST_9;



public class NUMEROS200_ATIV1 {

	
	public static void main(String[] args) {
        System.out.println("Quadrados dos números inteiros de 15 a 200:");
        
        for (int i = 15; i <= 200; i++) {
            int quadrado = i * i;
            System.out.println("O quadrado de " + i + " é: " + quadrado);
        }
    }
}
	
	
	
	
	
	
	
	

